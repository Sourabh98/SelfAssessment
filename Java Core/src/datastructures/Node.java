package datastructures;

public class Node {

	private int data;
	private Node nextNode;

	public int getData() {
		return data;
	}

	public void setData(int data) {
		this.data = data;
	}

	public Node getNextNode() {
		return nextNode;
	}

	public void setNextNode(Node node) {
		this.nextNode = node;
	}

}
